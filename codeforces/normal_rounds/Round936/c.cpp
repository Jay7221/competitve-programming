void solve() {
    int n, k;
    cin >> n >> k;
    vector<vector<int>> adj(n);
    for(int i = 0; i < n - 1; ++i){
        int u, v;
        cin >> u >> v;
        --u, --v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    function<int(int, int, int, int&)> dfs = [&](int u, int p, int x, int &cnt){
        int sz = 1;
        for(int v: adj[u]){
            if(v != p){
                sz += dfs(v, u, x, cnt);
            }
        }
        if(sz >= x){
            sz = 0;
            ++cnt;
        }
        return sz;
    };

    auto check = [&](int x){
        int cnt = 0;
        dfs(0, -1, x, cnt);
        return (cnt > k);
    };

    int l = 1, r = n;
    while(l < r){
        int m = (l + r + 1) >> 1;
        if(check(m)){
            l = m;
        }
        else{
            r = m - 1;
        }
    }

    cout << l << '\n';
}
 
int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
 
    int tc;
    cin >> tc;
    for(int t = 1; t <= tc; ++t){
//        cout << "# Case " << i << " : ";
        solve();
    }
    
    return 0;
}
 
