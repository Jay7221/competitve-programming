void solve(){
	int n;
	cin >> n;
	vector<int> a(n);
	for(int i = 0; i < n; i++)
		cin >> a[i];
	vector<int> pre(n);
	pre[0] = a[0];
	for(int i = 1; i < n; i++)
		pre[i] = a[i] + pre[i - 1];
	
}
