// RANDOM NUMBER GENERATOR
mt19937 RNG(chrono::steady_clock::now().time_since_epoch().count());  
#define SHUF(v) shuffle(all(v), RNG); 
// Use mt19937_64 for 64 bit random numbers.
 
 
void solve() {
    ll x1, y1, z1;
    ll x2, y2, z2;
    ll k;
    cin >> x1 >> y1 >> z1;
    cin >> x2 >> y2 >> z2;
    cin >> k;

    ll dx = abs(x1 - x2);
    ll dy = abs(y1 - y2);
    ll dz = abs(z1 - z2);

    ll res = dx + dy + dz;

    ll mx = max({dx, dy, dz});
    ll s = dx + dy + dz;

    ll no_switch = (mx + k - 1) / k - 1;
    ll avaliable = s - mx;

    ll req = max(0ll, no_switch - avaliable);
    if(req & 1){
        ++req;
    }
    res += req;

    cout << res << '\n';
}
 
int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
 
    int tc;
    cin >> tc;
    for(int t = 1; t <= tc; ++t){
//        cout << "# Case " << i << " : ";
        solve();
    }
    
    return 0;
}
 
